# while_loop_example.py
# This program demonstrates the use of a while loop in Python.

# Using a while loop to display numbers from 5 down to 1
counter = 5
print("Displaying numbers from 5 down to 1 using a while loop:")
while counter > 0:
    print(counter, end=" ")  # Print the current value of counter
    counter -= 1             # Decrement counter by 1 each time
print()  # Print a newline at the end
